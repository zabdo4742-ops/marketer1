"use client";

import { useState, useEffect } from "react";

export interface Client {
  id: string;
  name: string;
  phone: string;
  whatsapp: string;
  location: string;
  district: string;
  age: number;
  gender: "male" | "female";
  housingStatus: string;
  children: number;
  grades: string;
  interests: string[];
  notes: string;
  staffId: string; // Tracks which staff registered this client
  createdAt: string;
}

export interface Interest {
  id: string;
  label: string;
  category: string;
}

export interface CustomField {
  id: string;
  label: string;
  type: "text" | "number" | "date" | "select";
  active: boolean;
}

export interface Staff {
  id: string;
  name: string;
  phone: string;
  password?: string;
  role: string;
  status: "Active" | "Suspended" | "On Call" | "Offline";
  rating: number;
  tasksDone: number;
  monthlyTasks: number;
  image: string;
  active: boolean;
}

